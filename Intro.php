<script>
    var navbar =document.getElementById("navbar")
    var menu  =document.getElementById("menu")

    window.onscroll = function(){
        if(window.pageYOffset >= menu.offsetTop){
            navbar.classList.add("sticky");
        }
        else {
            navbar.classList.remove("sticky");  
        }
    }

    var modal = document.getElementById('login');
    window.onclick = function(event) {
        if (event.target == modal) {
            modal.style.display = "none";
        }
    }

    const wrapper = document.querySelector('.wrapper');
    const registerLink = document.querySelector('.register-link');
    const loginLink = document.querySelector('.login-link');
    registerLink.onclick = () => {
        wrapper.classList.add('active');
    }
    loginLink.onclick = () => {
        wrapper.classList.remove('active');
    }


    document.addEventListener("DOMContentLoaded", function() {
    var scrollButton = document.getElementById("scrollButton");
    var targetSection = document.getElementById("about");
    scrollButton.addEventListener("click", function(event) {
        event.preventDefault();
        
        var scrollToPosition = targetSection.offsetTop -126;
        window.scrollTo({ top: scrollToPosition, behavior: "smooth" });
    });
    });
    document.addEventListener("DOMContentLoaded", function() {
    var scrollButton2 = document.getElementById("scrollButton2");
    var targetSection2 = document.getElementById("service");
    scrollButton2.addEventListener("click", function(event) {
        event.preventDefault();
        
        var scrollToPosition2 = targetSection2.offsetTop -350;
        window.scrollTo({ top: scrollToPosition2, behavior: "smooth" });
    });
    });



